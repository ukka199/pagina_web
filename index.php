				<h2 class="El mejor Battle Royal"></h2>
				<p>
					Un nuevo género del shooter ha venido para quedarse, cada vez lo quieren implementar en más títulos pero no creo que desaparezca cuando la moda pase. PlayerUnknown´s Battleground es una propuesta que ha ido evolucionando con el tiempo y que voy analizar la situación en la que se encuentra ahora desde mi punto de vista.

					Uno de los aspectos más importantes es la jugabilidad en este videojuego se puede decir que es difícil manejar las armas; tienen retroceso y caída. Por eso son tan importantes los accesorios, pueden darte más ventaja. No es lo mismo tener un rifle sin nada que un rifle con cargador ampliado, mira, compensador, empuñadura, etc. En mi caso siempre que tengo un rifle, lo uso en modo semiautomático, es mucho más fácil de controlar y , dependiendo de la situación, un arma automática o a ráfagas para encuentros cercanos, o un franco si las distancias son muy largas. Pero no solo son cruciales las armas, también el equipamiento, tener mochila nos permitirá llevar mas cosas, los cascos y chalecos nos protegerán y las medicinas nos curarán. Hay muchas cosas que coger, por eso durante toda la partida es normal que siempre busquemos e incluso si vamos bien en todo, encontrar un chaleco nivel 3 sorprenderá.

					El juego en equipo se disfruta más, la comunicación es importante para intentar que no muera nadie. Se los digo yo porque no hay nada peor que ser el único al que han tirado y rematado del equipo por ir a tu rushear a lo loco y pasar del resto. Hay que observar, escuchar y comunicar a nuestros compañeros si pasa algo.

					En conclusión, un juego divertido con muchas posibilidades debido a todas las variantes que hay y la mejor opción de entre todos los Battle Royale sin lugar a dudas.
